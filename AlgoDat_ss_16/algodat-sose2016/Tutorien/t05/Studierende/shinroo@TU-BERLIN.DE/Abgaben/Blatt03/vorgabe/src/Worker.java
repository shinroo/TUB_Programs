/**
 * An employee at an enterprise
 * @author AlgoDat team
 *
 */
public class Worker {
	/** First name of worker */
    String firstName;
    /** Second name of worker */
    String lastName;
    
    /**
     * Creates a new worker with a given name
     * @param firstName First name of worker
     * @param lastName Last name of worker
     */
    public Worker(String firstName, String lastName) {
        this.firstName = firstName;
        this.lastName = lastName;		
    }
}

